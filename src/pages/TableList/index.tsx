import React, { useRef, useState } from 'react';
import type { FormValueType } from './components/UpdateForm';
import UpdateForm from './components/UpdateForm';

export type MockPageParams = {
  current?: number;
  pageSize?: number;
};

export type MockRuleListItem = {
  key?: number;
  disabled?: boolean;
  href?: string;
  avatar?: string;
  name?: string;
  owner?: string;
  desc?: string;
  callNo?: number;
  status?: number;
  updatedAt?: string;
  createdAt?: string;
  progress?: number;
};

export type MockRuleList = {
  data?: MockRuleListItem[];
  /** 列表的内容总数 */
  total?: number;
  success?: boolean;
};

