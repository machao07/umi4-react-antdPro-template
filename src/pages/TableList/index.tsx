/**
 * @en-US Add node
 * @zh-CN 添加节点
 * @param fields
 */
const mockHandleAdd = async (fields: MockRuleListItem) => {
  const hide = message.loading('正在添加');
  try {
    mockTableListDataSource = [{ ...fields }, ...mockTableListDataSource];
    hide();
    message.success('Added successfully');
    return true;
  } catch (error) {
    hide();
    message.error('Adding failed, please try again!');
    return false;
  }
};

/**
 * @en-US Update node
 * @zh-CN 更新节点
 *
 * @param fields
 */
const mockHandleUpdate = async (fields: FormValueType) => {
  const hide = message.loading('Configuring');
  try {
    //TODO use api
    for (let item of mockTableListDataSource) {
      if (fields.key == item.key) {
        item.name = fields.name;
        item.desc = fields.desc;
        item.key = fields.key;
        break;
      }
    }
    hide();

    message.success('Configuration is successful');
    return true;
  } catch (error) {
    hide();
    message.error('Configuration failed, please try again!');
    return false;
  }
};

/**
 *  Delete node
 * @zh-CN 删除节点
 *
 * @param selectedRows
 */
const mockHandleRemove = async (selectedRows: MockRuleListItem[]) => {
  const hide = message.loading('正在删除');
  if (!selectedRows) return true;
  try {
    //TODO use api
    mockTableListDataSource = mockTableListDataSource.filter((item) => {
      for (let di of selectedRows) {
        if (di.key == item.key) {
          return false;
        }
      }
      return true;
    });
    hide();
    message.success('Deleted successfully and will refresh soon');
    return true;
  } catch (error) {
    hide();
    message.error('Delete failed, please try again');
    return false;
  }
};
// mock tableListDataSource
const mockGenList = (current: number, pageSize: number) => {
  const tableListDataSource: MockRuleListItem[] = [];

  for (let i = 0; i < pageSize; i += 1) {
    const index = (current - 1) * 10 + i;
    tableListDataSource.push({
      key: index,
      disabled: i % 6 === 0,
      href: 'https://ant.design',
      avatar: [
        'https://gw.alipayobjects.com/zos/rmsportal/eeHMaZBwmTvLdIwMfBpg.png',
        'https://gw.alipayobjects.com/zos/rmsportal/udxAbMEhpwthVVcjLXik.png',
      ][i % 2],
      name: `TradeCode ${index}`,
      owner: '曲丽丽',
      desc: '这是一段描述',
      callNo: Math.floor(Math.random() * 1000),
      status: Math.floor(Math.random() * 10) % 4,
      updatedAt: '2023-01-01',
      createdAt: '2023-01-01',
      progress: Math.ceil(Math.random() * 100),
    });
  }
  tableListDataSource.reverse();
  return tableListDataSource;
};

let mockTableListDataSource = mockGenList(1, 100);
